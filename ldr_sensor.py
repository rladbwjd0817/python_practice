#2. 클래스 생성
class Ldr_sensor :
  
  # 생성자 및 멤버변수 같이 초기화
  # self : 이 클래스에 만들어진 객체 자기 자신을 정의하는 것
  def __init__(self):
    
    #3. 초기설정
    # 조도 값을 저장할 멤버변수 생성
    self.ldrSensor = 0
    # 라즈베리파이에서 센서랑 통신하는 도구인 spidev를 생성자 호출 및 멤버변수로 생성 -> 센서랑 연결 준비
    self.spi= spidev.SpiDev()
    # 이 센서가 정해진 시간동안 데이터를 읽을지 정하는 time 생성자 호출 및 멤버변수로 생성
    self.times = time.time()
    # GPIO가 몇 번 핀에 꽂혔는지 저장하는 channel 변수 생성
    self.ldr_channel= 0
    # 버스(통신 통로 번호, aka 도로)가 0, 
    # 디바이스(그 통로에 연결된 센서 번호, aka 그 도로의 집 주소)가 0 인 포트에 연결
    self.spi.open(0, 0)
    # 센서 연결 최대 속도는 100000 hz
    self.spi.max_speed_hz = 100000

  

  #4. 값을 읽는 함수 생성 - 매개변수에 self, 라즈베리파이에서 받아온 데이터를 저장할 변수
  def readadc(self, adcNum) :
    ## adcNum의 의미는 뭘까? 
    #   -> MCP3008은 채널이 0~7번, 총 8개 
    #   -> "몇 번 채널에서 값을 읽을래?"를 지정하는 번호
    ## 매개변수에 self를 꼭 넣어줘야 할까? -> YES!
    ## 이미 __init__ 함수에 self가 매개변수로 들어가있는데 저걸 끌어와서 쓸 수 있지 않나? -> 안됨! 두 개 다른 거임!
    ## => __init__의 self : 막 태어난 객체 자신 -> 속성 초기화에 씀
    ## => 일반 메서드의 self : 호출한 객체 자신 -> 저장해둔 속성 꺼내 씀

    # 만약 adcNum이 7을 초과하거나 0 미만이면
    ## 왜 adcNum의 기준을 7과 0으로 잡을까? 그 기준이 뭘까?
    #   -> MCP3008 채널은 0 ~ 7번 총 8개라서 가장 낮은 번호인 0, 가장 높은 번호인 7을 기준으로 정함
    if adcNum > 7 or adcNum < 0 :
      # -1 을 리턴해줌 
      ## 왜 -1을 리턴해줄까? 0보다 작은 숫자 중 가장 큰 숫자라서? 
      # -> 유효하지 않은 값을 표현하는 관례임! 센서 정상값은 항상 0 이상이니까 -1이 오면 "잘못됐다"라는 신호를 보낸거
      return -1
    
    ## r 에 이 클래스에서 정의된 센서에게 1과 adcNum에 8을 더한 숫자가 4보다 작은 값, 0의 값을 줘!! 라고 배열형식의 데이터를 요청보내는 것????
    # -> [시작 신호, 채널번호 인코딩, 응답받을 자리(빈 바이트)]
    # -> '<<' 가 '+' 보다 우선 순위임
    # 8 + adcNum << 4 : adcNum을 4비트 왼쪽으로 민 다음 8을 더함.
    # xfer2 : 센서에게 값줘! 라고 요청하면서 동시에 받음!
    r = self.spi.xfer2([1, 8 + adcNum << 4, 0]) 

    ## 그럼 데이터를 받는 코드는 바로 밑의 코드인 듯?
    ## data에 r에 저장된 배열의 1번 요소와 3이 8보다 작은 값과 r 배열의 2번 요소의 값을 저장한다?
    # -> 배열아님! MCP3008이 보내준 응답에서 실제 센서값(10비트)를 꺼내는 코드!
    # -> r[0] : 의미없는 바이트(버림)
    # -> r[1] : 하위 2비트만 유효 => r[1] & 3 으로 앞 비트들 날림
    # -> r[2] : 나머지 8비트(1바이트)
    # (r[1] & 3) << 8 : 상위 2비트를 8칸 왼쪽으로 밀고
    # + r[2] : 하위 8비트 붙임
    # 10비트 숫자 완성!(0 ~ 1023)
    data = ((r[1] & 3) << 8 + r[2])

    # data에 저장된 값을 return함.
    return data

  # 5. 우리가 진짜 사용할 함수 생성 - DB 연결도 여기서!
