# 비트 이동 : 2진수로 표현했을 때, 자리를 옮기는 것
# ex) adcNum = 1
# 2진수로 -> 00000001
# adcNum << 4 : 4비트 왼쪽으로 밀기
# 밀기 전 : 00000001
# 밀기 후 : 00010000
# 빈 자리엔 0이 채워지고, 원래 있던 1이 왼쪽으로 4칸 이동
# 1 << 4 = 16 => 10진수로 16
# '<<' 는 2의 n승과 같음
# ex) 1 << 4 = 1 * 2의 4승 = 1 * 16 = 16
# ex) 3 << 4 = 3 * 2의 4승 = 3 * 16 = 48
# MCP3008이 채널 번호를 '2번째 바이트의 상위 비트 위치'에서 읽기 때문에
# 2번째 바이트 : [ 1 | CH2 | CH1 | CH0 | x | x | x | x ]
#               8번위치                하위 4비트는 안 씀
# << 4 : 2 진수에서 왼쪽으로 4칸 이동 => * 16 -> MCP3008이 요구하는 비트 위치에 채널번호를 맞추기 위해 사용