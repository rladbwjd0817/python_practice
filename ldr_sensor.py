# 정해놓은 시간에 센서를 읽기 위해서 필요한 라이브러리
import time
# GPIO : 라즈베리파이의 물리적인 핀들을 제어하는 라이브러리, 
# 이 핀을 입력으로 쓸지 출력으로 쓸지 설정하고 제어
